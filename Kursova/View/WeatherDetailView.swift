                                        DailyForecastItemView(item: item)
                                    }
                                }
                                .padding(.horizontal)
                                .padding(.bottom, 20)
                                
                            }
                            
                            // –ó–∞–≥–ª—É—à–∫–∞, —è–∫—â–æ –¥–∞–Ω–∏—Ö –Ω–µ–º–∞—î
                            else if !viewModel.isLoading && viewModel.errorMessage == nil {
