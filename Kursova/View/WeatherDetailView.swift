                        .frame(minHeight: geometry.size.height - 100)
                        
                    }
                }
                .padding(.top, 10)
                .foregroundColor(.white)
                .shadow(color: .black.opacity(0.8), radius: 5, x: 0, y: 2)
                .background(Color.clear) 
            }
        }
